package com.scheduler.app.constants;

/**
 * The enum for API Request Statuses.
 */
public enum REQUEST_STATUS {

    /**
     * Success request status.
     */
    SUCCESS,

    /**
     * Error request status.
     */
    ERROR,

    /**
     * Invalid Request status.
     */
    INVALID_REQUEST,

    /**
     * Incorrect Password request status for login API.
     */
    INCORRECT_PASSWORD,

    /**
     * Bad request status.
     */
    BAD_REQUEST,

    /**
     * Failed request status.
     */
    FAILED
}
